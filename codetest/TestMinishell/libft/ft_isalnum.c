int	ft_isalnum(int ch)
{
	if (!(ch >= 'A' && ch <= 'Z'))
		if (!(ch >= 'a' && ch <= 'z'))
			if (!(ch >= '0' && ch <= '9'))
				return (0);
	return (1);
}
